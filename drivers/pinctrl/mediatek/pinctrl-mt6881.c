/* MT6881 have multiple bases to program pin configuration listed as the below:
 * GPIO_BASE: 0x10005000
 * IOCFG_BL_BASE: 0x11B00000
 * IOCFG_BL1_BASE: 0x11B10000
 * IOCFG_BM_BASE: 0x11B20000
 * IOCFG_RM_BASE: 0x11C00000
 * IOCFG_BR_BASE: 0x11D40000
 * IOCFG_BR1_BASE: 0x11D60000
 * IOCFG_LM_BASE: 0x11E20000
 * IOCFG_RT_BASE: 0x11EC0000
 * IOCFG_LT_BASE: 0x11F30000
 * _i_based could be used to indicate what base the pin should be mapped into.
 */

